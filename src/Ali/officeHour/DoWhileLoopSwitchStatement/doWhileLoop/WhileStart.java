package Ali.officeHour.DoWhileLoopSwitchStatement.doWhileLoop;

public class WhileStart {

    public static void main(String[] args) {

        /*
        For loop: If you want to run one code multiple times then you need to use loops

         */

        // while loop

        int i = 11;

        while(i<10){

            System.out.println("hello world");
            System.out.println(i);
            i++;
        }

        System.out.println(i);
        System.out.println("code is ended");
    }

}
